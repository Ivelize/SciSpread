package pojo;

public class CategoryPOJO {
	
	private String categoryType;
	private Integer occurrenceNumber;
	private String spreadsheetName;
	private Float percScheme;
	private Integer rowScheme;
	
	public String getCategoryType() {
		return categoryType;
	}
	
	public void setCategoryType(String categoryType) {
		this.categoryType = categoryType;
	}
	
	public Integer getOccurrenceNumber() {
		return occurrenceNumber;
	}
	
	public void setOccurrenceNumber(Integer occurrenceNumber) {
		this.occurrenceNumber = occurrenceNumber;
	}

	public String getSpreadsheetName() {
		return spreadsheetName;
	}

	public void setSpreadsheetName(String spreadsheetName) {
		this.spreadsheetName = spreadsheetName;
	}

	public Float getPercScheme() {
		return percScheme;
	}

	public void setPercScheme(Float percScheme) {
		this.percScheme = percScheme;
	}

	public Integer getRowScheme() {
		return rowScheme;
	}

	public void setRowScheme(Integer rowScheme) {
		this.rowScheme = rowScheme;
	}

	

}
